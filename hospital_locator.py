def get_hospitals_for_city(city):
    if city not in city_bounds:
        return []

    bbox = city_bounds[city]
    url = f"{GEOAPIFY_URL}?categories=healthcare.hospital&filter={bbox}&limit=20&apiKey={GEOAPIFY_API_KEY}"
    headers = {"User-Agent": "Mozilla/5.0"}
    response = requests.get(url, headers=headers)

    if response.status_code != 200:
        return []

    data = response.json().get("features", [])
    results = []

    for f in data:
        props = f["properties"]
        name = props.get("name", "Unknown Hospital")
        address = props.get("formatted", "No address available")
        lon = props.get("lon")
        lat = props.get("lat")
        rating = round(random.uniform(3.0, 5.0), 1)
        stars = "‚≠ê" * int(round(rating))
        map_url = f"https://www.google.com/maps?q={lat},{lon}" if lon and lat else None

        results.append({
            "name": name,
            "address": address,
            "rating": rating,
            "stars": stars,
            "map_url": map_url
        })
    return results
